first_input = int(input("Enter the first number: "))
second_input = int(input("Enter the second number: "))

sum = first_input + second_input
difference = first_input - second_input
product = first_input * second_input
division = first_input / second_input
power = first_input ** second_input

print(f"The sum of the two numbers are {sum}")
print("The difference of the two numbers are {p}".format(p=difference))
print(f"The product of the two numbers are {product}")
print(f"The division of the two numbers are {division}")
print("{first_input} raised to the power of {second_input} is {p}".format(first_input= first_input, second_input=second_input,  p=power))
